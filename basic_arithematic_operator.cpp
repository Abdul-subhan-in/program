#include<iostream>

int main()
{
    int a = 20;
    int b = 5;
    int sum = a+b;
    int difference = a-b;
    int multiple = a*b;
    int Quotient = a/b;
    std::cout<<"The sum is "<<sum<<std::endl;
    std::cout<<"The difference is "<<difference<<std::endl;
    std::cout<<"The multiple is "<<multiple<<std::endl;
    std::cout<<"The quotient is "<<Quotient;
    //Output
    //The sum is 25
    //The difference is 15
    //The multiple is 100
    //The quotient is 4 
}